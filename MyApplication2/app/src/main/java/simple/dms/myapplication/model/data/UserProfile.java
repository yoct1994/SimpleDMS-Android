package simple.dms.myapplication.model.data;

public class UserProfile {
    private String advice;
    private int badPoint;
    private int goodPoint;
    private String name;
    private int number;
    private int penaltyLevel;
    private boolean penaltyStatus;

    public String getAdvice() {
        return advice;
    }

    public int getBadPoint() {
        return badPoint;
    }

    public int getGoodPoint() {
        return goodPoint;
    }

    public String getName() {
        return name;
    }

    public int getNumber() {
        return number;
    }

    public int getPenaltyLevel() {
        return penaltyLevel;
    }

    public boolean isPenaltyStatus() {
        return penaltyStatus;
    }
}
